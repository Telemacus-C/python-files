number1 = float(input('What is your number?: '))

number2 = float(input('What is your other number?: '))

def function_divide(x, y):
    return x / y
def function_Idivide(x, y):
    return x // y
def modulus(x, y):
    return x % y

print (function_divide(number1, number2))
print (function_Idivide(number1, number2))
print (modulus(number1, number2))
